class switch {
    public static void main(String[] args) {
        int opcao;
        String resultado;
        switch (opcao){
            case 1:
            resultado = "opçao 1";
            break;
            case 2:
            resultado = "opçao 2";
            break;
            case 3:
            resultado = "opçao 3";
            break;
            case 4:
            resultado = "opçao 4";  
            break;
            case 5:
            resultado = "opçao 5";
            break;
            default:
            resultado = "opçao invalida";
            break;

            System.out.println("+resultado");

        }
    }
}
