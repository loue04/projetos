class exemplo_operadores {
    public static void main(String[] args) {
        int A;
        int B;

        int soma;
        int subtracao;
        int multiplicacao;
        int divisao;
        int resto_de_divisao;

        A = 4;
        B = 2;

        soma = A + B;
        subtracao = A - B;
        multiplicacao = A * B;
        divisao= A / B;
        resto_de_divisao = A%B;

        System.out.println("soma:" + soma);
        System.out.println("subtração:" + subtracao);
        System.out.println("multiplicação:" + multiplicacao);
        System.out.println("divisao" + divisao);
        System.out.println("resto de divisão:" + resto_de_divisao);

    }

}